int	ex05_add(int value1, int value2)
{
	return (value1 + value2);
}

int	ex05_sub(int value1, int value2)
{
	return (value1 - value2);
}

int	ex05_mul(int value1, int value2)
{
	return (value1 * value2);
}

int	ex05_div(int value1, int value2)
{
	return (value1 / value2);
}

int	ex05_mod(int value1, int value2)
{
	return (value1 % value2);
}
